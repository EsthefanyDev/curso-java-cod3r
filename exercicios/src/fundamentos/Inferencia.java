package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		var b = 4.5;
		System.out.println(b);
		
		var c = "TExto";
		System.out.println(c);
		
		c = "Outro texto";
		System.out.println(c);
		
		double d;
		d = 234.56;
		System.out.println(d);
	}
}
